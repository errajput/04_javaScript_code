const add = (a, b) => {
  let results = a + b;
  return results;
};
console.log(add(10, 2));

const sub = (n, m) => {
  let results = n - m;
  return results;
};
console.log(sub(10, 2));

const multiple = (l, o) => {
  let results = l * o;
  return results;
};
console.log(multiple(10, 2));

const division = (p, q) => {
  let results = p / q;
  return results;
};
console.log(division(10, 2));
